
class EsercizioWhile_2_Felicio
{
	//Esercizio 2 Calcolare la somma dei numeri da 1 a 100 utilizzando il ciclo "while"


public static void main (String args[])

	{
	
		int i=0;
		int risultato=0;

		while(i<=100){
			risultato=risultato+i;
			i++;
		}
		System.out.print(" " + risultato);

	}

	
}

